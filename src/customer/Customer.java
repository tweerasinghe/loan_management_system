    private void sc_keywordKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_sc_keywordKeyPressed
        if (evt.getKeyCode() == 10) {
            if (!UserLogin.userPrivilage.contains(UI_NAME3)) {
                JOptionPane.showMessageDialog(this, "Access Denied", "Info", JOptionPane.OK_OPTION);
            } else {
                String keyword = sc_keyword.getText();
                if (keyword.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Enter Keyword", "Info", JOptionPane.OK_OPTION);
                    sc_keyword.grabFocus();
                } else {
                    ResultSet rs = null;
                    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                    dtm.setRowCount(0);
                    rowCount.setText("Row Count - ");
                    try {
                        String sql = null;
                        if (keyword == "0000") {
                            sql = "select * from customer where custoemer_nic LIKE '%" + keyword + "%' and status='1';";
                        } else {
                            sql = "select * from customer where status='1';";
                        }

                        rs = jdbc.getData(sql);
                        while (rs.next()) {
                            Vector vector = new Vector();
                            vector.add(rs.getInt("customer_id"));
                            vector.add(rs.getString("custoemer_nic"));
                            vector.add(rs.getString("name_ini"));
                            vector.add(rs.getString("name_full"));
                            vector.add(rs.getString("gender"));
                            vector.add(rs.getString("no"));
                            vector.add(rs.getString("street"));
                            vector.add(rs.getString("city"));
                            vector.add(rs.getString("contact1"));
                            vector.add(rs.getString("contact2"));
                            vector.add(rs.getString("email"));
                            vector.add(rs.getString("addedDate"));
                            vector.add(rs.getString("specialNote"));

                            ResultSet rs2 = jdbc.getData("select name,status from user where user_id = '" + rs.getInt("user_user_id") + "'  ");
                            if (rs2.next()) {
                                if (rs2.getBoolean("status")) {
                                    vector.add(rs2.getString("name"));
                                } else {
                                    vector.add(rs2.getString("name") + "(DELETED)");
                                }
                            }
                            vector.add(rs.getString("folderid"));

                            dtm.addRow(vector);

                        }

                        rowCount.setText("Row Count - " + jTable1.getRowCount());
                    } catch (Exception e) {
                        e.printStackTrace();
                    }

                    if (rs == null) {
                        JOptionPane.showMessageDialog(this, "Not Found", "Info", JOptionPane.OK_OPTION);
                    }

                }
            }
        }
    }//GEN-LAST:event_sc_keywordKeyPressed

