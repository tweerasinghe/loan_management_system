    private void jLabel11MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel11MouseClicked
        if (evt.getButton() == 1) {

            boolean myFlag = UserLogin.userPrivilage.contains(UI_NAME1);
            if (!myFlag) {
                JOptionPane.showMessageDialog(this, "Access Denied", "Info", JOptionPane.OK_OPTION);
            } else if (customerID == 000) {
                JOptionPane.showMessageDialog(this, "No User", "Info", JOptionPane.OK_OPTION);
            } else {
                String initial = uc_initial.getText();
                String fullName = uc_fullName.getText();
                String nic = uc_nic.getText();
                String no = uc_no.getText();
                String street = uc_street.getText();
                String city = uc_city.getText();
                String contact1 = uc_contact1.getText();
                String contact2 = uc_contact2.getText();
                String email = uc_email.getText();
                String special = uc_special.getText();
                String folderID = uc_folderID.getText();
                String gender = (uc_male.isSelected()) ? "Male" : "Female";

                if (initial.isEmpty() || nic.isEmpty() | fullName.isEmpty() || no.isEmpty() || street.isEmpty() || city.isEmpty() || contact1.isEmpty() | contact2.isEmpty() | email.isEmpty() | special.isEmpty() | folderID.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Fill All Details", "Info", JOptionPane.OK_OPTION);
                } else if (contact1.length() != 10) {
                    JOptionPane.showMessageDialog(this, "Check Contact 1 Length", "Info", JOptionPane.OK_OPTION);
                    uc_contact1.grabFocus();
                    uc_contact1.selectAll();
                } else if (contact2.length() != 10) {
                    JOptionPane.showMessageDialog(this, "Check Contact 2 Length", "Info", JOptionPane.OK_OPTION);
                    uc_contact2.grabFocus();
                    uc_contact2.selectAll();
                } else {

                    boolean flag = true;
                    try {
                        ResultSet rs = jdbc.getData("select customer_id from customer where custoemer_nic='" + nic + "' and customer_id != '" + customerID + "' and status='1' ");
                        if (rs.next()) {
                            flag = false;
                        }

                    } catch (Exception e) {
                        e.printStackTrace();
                    }

                    if (flag && (JOptionPane.showConfirmDialog(this, "UpdateCustomer ?", "Info", JOptionPane.OK_CANCEL_OPTION) == 0)) {
                        try {
                            String sql = "update customer set custoemer_nic='" + nic + "', name_full='" + fullName + "', name_ini='" + initial + "', gender='" + gender + "', no='" + no + "', street='" + street + "', city='" + city + "', contact1='" + contact1 + "', contact2='" + contact2 + "', email='" + email + "', specialNote='" + special + "', folderid='" + folderID + "', imageName='" + imageName + "' where customer_id='" + customerID + "'  ";
                            int status = jdbc.putData(sql);
                            if (status == 1) {
                                JOptionPane.showMessageDialog(this, "Data Updated", "Info", JOptionPane.INFORMATION_MESSAGE);
                                refreshUpdate();
                            }
                        } catch (Exception e) {
                            e.printStackTrace();
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Duplicate NIC", "Info", JOptionPane.OK_OPTION);

                    }

                }

            }

        }
    }//GEN-LAST:event_jLabel11MouseClicked
