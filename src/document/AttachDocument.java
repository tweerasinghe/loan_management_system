    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        String id = jComboBox1.getSelectedItem().toString();
        if (id != "Select Customer NIC") {
            try {
                ResultSet rs = jdbc.getData("select name_full,gender,contact1,no,street,city from customer where custoemer_nic = '" + id + "'   ");
                if (rs.next()) {
                    cu_name.setText(rs.getString("name_full"));
                    cu_gender.setText(rs.getString("gender"));
                    cu_contact1.setText(rs.getString("contact1"));
                    cu_no.setText(rs.getString("no"));
                    cu_street.setText(rs.getString("street"));
                    cu_city.setText(rs.getString("city"));

                }
            } catch (Exception e) {
                e.printStackTrace();
            }

            try {
                ResultSet rs = jdbc.getData("select customer_id from customer where custoemer_nic='" + id + "';");
                if (rs.next()) {
                    customerID = rs.getInt("customer_id");
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

            loadDoc(customerID);

        } else {
            customerID = 000;
            cu_name.setText(null);
            cu_gender.setText(null);
            cu_contact1.setText(null);
            cu_no.setText(null);
            cu_street.setText(null);
            cu_city.setText(null);
            ((DefaultTableModel) jTable1.getModel()).setRowCount(0);
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed
    private void loadDoc(int id) {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        try {
            ResultSet rs = jdbc.getData("select iddocument_details,details from document_details where customer_id='" + id + "' and status='1';");
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getInt("iddocument_details"));
                v.add(rs.getString("details"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
