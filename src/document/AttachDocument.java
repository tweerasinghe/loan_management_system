    private void jLabel13MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel13MouseClicked

        if (!UserLogin.userPrivilage.contains(UI_NAME)) {
            JOptionPane.showMessageDialog(this, "Access Denied", "Info", JOptionPane.OK_OPTION);
        } else {

            String keyword = docName.getText();
            if (customerID == 000) {
                JOptionPane.showMessageDialog(this, "Select Customer NIC", "Info", JOptionPane.OK_OPTION);
            } else if (keyword.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Enter Document Name", "Info", JOptionPane.OK_OPTION);
            } else {
                try {
                    int status = jdbc.putData("insert into document_details (details, status, customer_id) values ('" + keyword + "','1','" + customerID + "') ");
                    JOptionPane.showMessageDialog(this, "Saved", "Info", JOptionPane.INFORMATION_MESSAGE);
                    loadDoc(customerID);
                    docName.setText(null);
                    docName.grabFocus();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_jLabel13MouseClicked

