    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        if (evt.getButton() == 1) {
            if (!UserLogin.userPrivilage.contains(UI_NAME1)) {
                JOptionPane.showMessageDialog(this, "Access Denied", "Info", JOptionPane.OK_OPTION);
            } else if (customerID == 000) {
                JOptionPane.showMessageDialog(this, "Select Customer", "Info", JOptionPane.OK_OPTION);
            } else if (jTable1.getSelectedRowCount() <= 0) {
                JOptionPane.showMessageDialog(this, "Select Deocument/s", "Info", JOptionPane.OK_OPTION);
            } else if (jTable1.getSelectedRowCount() >= 1) {
                if (JOptionPane.showConfirmDialog(this, "Delete Document/s ?", "Info", JOptionPane.OK_CANCEL_OPTION) == 0) {

                    int[] rows = jTable1.getSelectedRows();
                    int status = 0;
                    for (int row : rows) {
                        int docID = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
                        try {
                            status = jdbc.putData("update document_details set status='0' where iddocument_details='" + docID + "' and customer_id='" + customerID + "'  ");
                        } catch (Exception e) {
                            e.printStackTrace();
                        }

                    }
                    loadDoc(customerID);
                    if (status == 1) {
                        JOptionPane.showMessageDialog(this, "Data Deleted", "Info", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            }
        }
    }//GEN-LAST:event_jLabel6MouseClicked

